package com.kyd.springmachine.bean;

/**
 * 使用在orderActivity中的显示支付类型
 * @author 8015
 *
 */
public class PayInfo {
	private int payName;
	private int payIcon;
	public int getPayName() {
		return payName;
	}
	public void setPayName(int payName) {
		this.payName = payName;
	}
	public int getPayIcon() {
		return payIcon;
	}
	public void setPayIcon(int payIcon) {
		this.payIcon = payIcon;
	}
	
	
}
